    private var histories = TableViewController.getHistory()
    
    private static func getHistory() -> [String] {
        let histories = UserDefaults.standard.array(forKey: "history")
        if histories == nil {
            return Array()
        } else {
            return histories! as! [String]
        }
    }
    
